package Operators;

public class Relational {
    public static void main(String[] args) {
        int A = 10;
        int B = 5;
        int C = 10;

        System.out.println(A==B);
        System.out.println(A==C);
        System.out.println(A!=B);
        System.out.println(A!=C);
        System.out.println(A>B);
        System.out.println(B>A);
        System.out.println(A>C);
        System.out.println(A<B);
        System.out.println(B<A);
        System.out.println(A<A);
        System.out.println(A>=B);
        System.out.println(A>=C);
        System.out.println(A<=B);
        System.out.println(A<=C);

    }
}
